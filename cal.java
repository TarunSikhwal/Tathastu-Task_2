// Write a Java program to perform basic Calculator operations.
// ADD SUB MUL DIV
public class cal {
	public static void main(String args[]) {
		int a,b;
		int add, sub, mul, div;
		a = 5;
		b = 10;
		add = a + b;
		sub = a - b;
		mul = a * b;
		div = a / b;
		System.out.println("ADD:"+add);
		System.out.println("SUB:"+sub);
		System.out.println("MUL:"+mul);
		System.out.println("DIV:"+div);
		

	}
}
